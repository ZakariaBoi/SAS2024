#include <stdio.h>

int main() {
    float a;
    float b;
    printf("Enter The Number A : ");
    scanf("%f", &a);
    printf("Enter The Number B : ");
    scanf("%f", &b);
    printf("A + B = %f\n",a + b);
    printf("A - B = %f\n",a - b);
    printf("A * B = %f\n",a * b);
    printf("A / B = %f\n",a / b);
    return 0;
}